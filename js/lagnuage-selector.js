const enLanguageButton = document.querySelector("#en-language");
const plLanguageButton = document.querySelector("#pl-language");
const elementsToTranslate = document.querySelectorAll("[data-translate]");

let currentLanguage = localStorage.getItem("language") || "en";

function translateElements(language) {
  elementsToTranslate.forEach((element) => {
    const translationKey = element.dataset.translate;
    element.textContent = translations[language][translationKey];
  });
}

const switchLanguage = (language) => {
  currentLanguage = language;
  localStorage.setItem("language", language);
  translateElements(currentLanguage);
};

function langCheck() {
  if (currentLanguage === "en") {
    enLanguageButton.style.display = "none";
    plLanguageButton.style.display = "block";
  }

  if (currentLanguage === "pl") {
    plLanguageButton.style.display = "none";
    enLanguageButton.style.display = "block";
  }
}

plLanguageButton.addEventListener("click", () => {
  if (currentLanguage !== "pl") {
    switchLanguage("pl");
    langCheck();
  }
});

enLanguageButton.addEventListener("click", () => {
  if (currentLanguage !== "en") {
    switchLanguage("en");
    langCheck();
  }
});

translateElements(currentLanguage);
langCheck();
